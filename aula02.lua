-- aula 02 - variaveis, strings e concatenação

-- criando variavel e substituindo por outra
olaMundo = "Ola Mundo"
olaMundo = "Olá Mundo!"

-- imprimindo a variável
print(olaMundo)

-- imprimindo variavel/operação
operacao = 45*10
print(operacao)

-- usando o sinal __ na variavel um_numero_qualquer
um_numero_qualquer = 18
print(um_numero_qualquer)

-- concatenação/ imprimir as variaveis juntas com separação entre elas
print(olaMundo .. " - ".. operacao .. " " .. um_numero_qualquer)

--para concatenar variaveis com numeros inteiros
num1 = 2
num2 = 3

print(num1 + num2)

